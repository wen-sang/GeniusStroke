# 文件: config/null_handling.py
"""
空值处理策略配置

定义不同字段类型的空值处理规则，确保数据处理的一致性和准确性。
"""

# ==============================================================================
# 行情数据空值处理规则
# ==============================================================================

MARKET_DATA_NULL_STRATEGY = {
    # 价格字段：不填充，保留 NULL
    # 原因：0 价格是异常值，NULL 表示数据缺失更准确
    'open': 'keep_null',
    'high': 'keep_null',
    'low': 'keep_null',
    'close': 'keep_null',
    
    # 成交量/金额：填充为 0
    # 原因：0 表示无成交，符合业务语义
    'volume': 'fill_zero',
    'amount': 'fill_zero',
    
    # 字符串字段：不应该有 NULL，必须校验
    'asset_code': 'raise_error_if_null',
    'trade_date': 'raise_error_if_null',
    'source_id': 'fill_default',  # 填充默认值
}

# 默认值映射
DEFAULT_VALUES = {
    'source_id': 'unknown',
}

# ==============================================================================
# 指标数据空值处理规则
# ==============================================================================
# 技术指标：保留 NULL（pandas_ta 在边界可能返回 NaN）
INDICATOR_NULL_STRATEGY = 'keep_null'

# ==============================================================================
# 基本面数据空值处理规则
# ==============================================================================
# 基本面数据：保留 NULL（某些时间段可能无估值数据）
FUNDAMENTAL_NULL_STRATEGY = 'keep_null'
